      WHEN order_count + 1 >= 10 THEN 10  -- 設置為銅牌
                    ELSE 0  -- 普通用戶
                  END
  WHERE user_id = NEW.user_id;
END
$$
DELIMITER ;
DROP TRIGGER IF EXISTS `after_order_states_change`;
DELIMITER $$
CREATE TRIGGER `after_order_states_change` AFTER UPDATE ON `orders` FOR EACH ROW BEGIN
  -- 檢查訂單狀態是否從「已取消」變為「待支付」或「已支付」
  IF OLD.status = '已取消' AND (NEW.status = '待支付' OR NEW.status = '已支付') THEN
    -- 更新用戶的 order_count，增加1
    UPDATE users
    SET 
      order_count = order_count + 1,
      vip_level = CASE
        WHEN order_count + 1 >= 50 THEN 30  -- 設置為金牌
        WHEN order_count + 1 >= 30 THEN 20  -- 設置為銀牌
        WHEN order_count + 1 >= 10 THEN 10  -- 設置為銅牌
        ELSE 0  -- 普通用戶
      END
    WHERE user_id = NEW.user_id;
  END IF;
  
  -- 檢查訂單狀態是否從「待支付」或「已支付」變為「已取消」
  IF OLD.status <> '已取消' AND NEW.status = '已取消' THEN
    -- 更新用戶的 order_count，減少1
    UPDATE users
    SET 
      order_count = order_count - 1,
      vip_level = CASE
        WHEN order_count - 1 >= 50 THEN 30  -- 設置為金牌
        WHEN order_count - 1 >= 30 THEN 20  -- 設置為銀牌
        WHEN order_count - 1 >= 10 THEN 10  -- 設置為銅牌
        ELSE 0  -- 普通用戶
      END
    WHERE user_id = NEW.user_id;
  END IF;
END
$$
